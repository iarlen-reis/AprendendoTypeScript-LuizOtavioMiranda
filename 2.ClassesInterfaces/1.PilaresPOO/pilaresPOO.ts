/*
Pilares da programação orientada a objetos:
- Abstração
- Encapsulamento
- Herança
- Polimorfismo

- Abrastração:
    - Permite isolar de um objeto somente os conceitos
        que são necessários para o funcionamento do programa.

- Encapsulamento:
    - Visa ocultar partes internas de um objeto e exibir apenas
        o necessário para uso externo.

- Herança:
    - Visa passar caracteristicas de um objeto para o outro.

- Polimorfismo:
    - Algo que é sempre polimorfo tem a habilidade de assumir
        diferentes formas.

*/
